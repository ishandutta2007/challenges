int
filter(int f, int v)
{
	int b;

	if (v < 1 || v > 9)
		return -1;

	b = 1 << v;
	if (f & b)
		return -1;
	return f | b;
}

bool
sudoku(size_t n, int m[n][n])
{
	static const int mask = 0x3fe;
	size_t i, j, k, l;
	int r, c, d;

	if (n == 0 || (n % 3))
		return false;

	for (i = 0; i < n; i++) {
		r = c = 0;
		for (j = 0; j < n; j++) {
			if ((r = filter(r, m[j][i])) < 0)
				return false;
			if ((c = filter(c, m[i][j])) < 0)
				return false;
		}
		if (r != mask || c != mask)
			return false;
	}

	for (i = 0; i < n; i += 3) {
		for (j = 0; j < n; j += 3) {
			d = 0;
			for (k = 0; k < 3; k++) {
				for (l = 0; l < 3; l++) {
					if ((d = filter(d, m[i + k][j + l])) < 0)
						return false;
				}
			}
			if (d != mask)
				return false;
		}
	}

	return true;
}

