func common(a, b []int) []int {
	var p []int
	for i, j := 0, 0; i < len(a) && j < len(b); {
		switch {
		case a[i] < b[j]:
			i++
		case a[i] > b[j]:
			j++
		default:
			p = append(p, a[i])
			i++
			j++
		}
	}
	return p
}

func eq(a, b []int) {
	assert(len(a) == len(b))
	for i := range a {
		assert(a[i] == b[i])
	}
}

