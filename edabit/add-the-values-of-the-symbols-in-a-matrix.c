#include <assert.h>
#include <stdio.h>
#include <string.h>

#define nelem(x) (sizeof(x) / sizeof(x[0]))

enum {
	SIZE = 32,
};

int
score(const char *s[][SIZE], size_t n)
{
	static const struct {
		const char *sym;
		int val;
	} tab[] = {
	    {"#", 5},
	    {"O", 3},
	    {"X", 1},
	    {"!", -1},
	    {"!!", -3},
	    {"!!!", -5},
	};

	size_t i, j, k;
	int r;

	r = 0;
	for (i = 0; i < n; i++) {
		for (j = 0; s[i][j]; j++) {
			for (k = 0; k < nelem(tab); k++) {
				if (!strcmp(s[i][j], tab[k].sym)) {
					r += tab[k].val;
					break;
				}
			}
		}
	}

	return (r >= 0) ? r : 0;
}

