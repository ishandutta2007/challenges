#!/usr/bin/env/python

"""
Task
Read two integers and print two lines.
The first line should contain integer division, a // b.
The second line should contain float division, a / b.

You don't need to perform any rounding or formatting operations.
"""

def xdiv(a, b):
    print(a // b)
    print(a * 1.0 / b)

xdiv(4, 3)
