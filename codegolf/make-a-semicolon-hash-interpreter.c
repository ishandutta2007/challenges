#include <stdio.h>

void
interpret(const char *s)
{
	unsigned ac;
	size_t i;

	ac = 0;
	for (i = 0; s[i]; i++) {
		switch (s[i]) {
		case ';':
			ac++;
			break;
		case '#':
			printf("%c", ac % 127);
			ac = 0;
			break;
		}
	}
}

